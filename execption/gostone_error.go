type GoStoneError struct {
	Code    int    `json:"-"`
	Message string `json:"message"`
}

func NewGoStoneError(code int, message string) GoStoneError {
	return GoStoneError{
		Code:    code,
		Message: message,
	}
}

func (g *GoStoneError) Error() string {
	return g.Message
}
